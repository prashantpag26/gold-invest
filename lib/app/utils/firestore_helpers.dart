import 'package:cloud_firestore/cloud_firestore.dart';

/// Safely convert a Firestore field into a [DateTime].
/// Handles [Timestamp], ISO strings, and millisecond ints; returns null otherwise.
DateTime? dateFromFirestore(dynamic value) {
  if (value == null) return null;
  if (value is Timestamp) return value.toDate();
  if (value is DateTime) return value;
  if (value is String) return DateTime.tryParse(value);
  if (value is int) return DateTime.fromMillisecondsSinceEpoch(value);
  return null;
}

double? doubleFromFirestore(dynamic value) {
  if (value == null) return null;
  if (value is num) return value.toDouble();
  if (value is String) return double.tryParse(value);
  return null;
}

int intFromFirestore(dynamic value, {int fallback = 0}) {
  if (value is num) return value.toInt();
  if (value is String) return int.tryParse(value) ?? fallback;
  return fallback;
}
